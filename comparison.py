x=10
y=90
print(f"equal to: {x} and {y} is {x==y}")
print(f"not equal to: {x} and {y} is {x!=y}")
print(f"greater than: {x} and {y} is {x>y}")
print(f"less than: {x} and {y} is {x<y}")
print(f"greater than or equal to: {x} and {y} is {x>=y}")
print(f"less than or equal to: {x} and {y} is {x<=y}")